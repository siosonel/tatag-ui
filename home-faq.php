  <dd class="accordion-navigation">
    <a href="#panel1b">Isn't this just a barter network?</a>
    <div id="panel1b" class="content">
		<p>No, this is beyond bartering. For example, the welcome promotion created for a new team translates to a 'donation' of support - paying for that is definitely not barter. Also, unlike barter networks that specify how much a participant or team receives in in-network credits, Tatag lets each team create its own spendable credits and planned obligations via its reported budgets.</p>
    </div>
  </dd>
  <dd class="accordion-navigation">
    <a href="#panel2b">How do you avoid inflation?</a>
    <div id="panel2b" class="content">
      <ol>
      	<li><b>Payments are not guaranteed to be acceptable</b>, so there is an incentive for each team to maintain reasonable budgets. Otherwise, Advisor apps would generally reject payments coming from teams whose budget are evaluated to be not sustainable.</li>

	    <li><b>Budgets get used up and do not 'circulate' or keep on increasing.</b> A transaction cancels corresponding units of the payer's expense budget and the recipient's revenue budget. In combination with periodic budget issuance, transactions lead to a cyclic pattern on the total available budget among all teams. In this way, each team contributes to a decentralized regulation of currency activty. See the <a href='https://tatag.cc/ui/home-about#budgets-as-currency'>budgets-as-currency viz</a>.</li>

	    <li>For defunct teams that have excess unused revenue budget, the platform will develop a plan to pay those obligations with credits taken from one or more of the following: insurance-like system, collecting proportional credits from those who received payments from the defunct teams, etc.</li>
      </ol>
    </div>
  </dd>
  <dd class="accordion-navigation">
    <a href="#panel3b">Why would a seller refuse a payment?</a>
    <div id="panel3b" class="content">
	    <p>One reason is similar to having coupon restrictions: <b>a team would typically limit its commitments</b> to the platform as far as providing goods and services. In fact, recipient tokens in Tatag are created with default restrictions on how often it's used by a user, team, or overall within a seven day window. A user may also be forced to wait a given number of hours before beign able to reuse a recipient token to initiate a payment.</p>

		<p>Another reason is that <b>the seller already has spendable funds</b> via its expense budget, so there is not as much pressure to blindly accept payments regardless of the issuing team's reputation. A team can be more selective on who benefits from its goods and services.</p>

		<p>Lastly, but most important, the ability to refuse a payment signifies a participant's <b>power to influence</b> the market towards sustainable activity. See the <a href='https://tatag.cc/ui/home-about#influence'>influence-the-future viz</a>.</p>
    </div>
  </dd>
